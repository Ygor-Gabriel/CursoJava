public class OperadoresLogicos {

    public static void main(String[] args) {
        int x, y, z;
        x = 4;
        y = 7;
        z = 12;
        boolean r;
        // verificando se as duas estão felizes r = (x<y && y==z)?true:false;
        // verificando se uma ou outra está feliz r = (x<y || y==z)?true:false;
        //verificando se uma ou exclusivamente a outra está feliz r = (x<y  y==z)?true:false;
        
        System.out.println(r);
    }
}
